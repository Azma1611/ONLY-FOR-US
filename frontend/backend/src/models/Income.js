import mongoose from 'mongoose';

const incomeSchema = new mongoose.Schema({
  owner: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true,
    index: true,
  },
  source: {
    type: String,
    required: true,
    trim: true,
  },
  amount: {
    type: Number,
    required: true,
    min: 0,
  },
}, {
  timestamps: true,
});

const Income = mongoose.model('Income', incomeSchema);
export default Income;
